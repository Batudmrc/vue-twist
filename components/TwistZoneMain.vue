      function marqueeSlider(o) {
        o = o || {};

        var ID = o["ID"],
          direction = o["direction"] || "left";

        var mainTicker = new Flickity(ID, {
          accessibility: true,
          resize: true,
          wrapAround: true,
          prevNextButtons: false,
          pageDots: false,
          percentPosition: true,
          setGallerySize: true,
        });

        var requestId = null;

        mainTicker.x = 0;

        play();

        function play() {
          if (direction == "left") mainTicker.x -= 1.5;
          else mainTicker.x += 1.5;

          mainTicker.settle(mainTicker.x);

          requestId = window.requestAnimationFrame(play);
        }

        function pause() {
          if (requestId) {
            window.cancelAnimationFrame(requestId);
            requestId = undefined;
          }
        }

        $(ID).on("mouseenter focusin", function (e) {
          pause();
        });

        $(ID).on("mouseleave", function (e) {
          play();
        });
      }

      function shuffleArray(array) {
        for (var i = array.length - 1; i > 0; i--) {
          var j = Math.floor(Math.random() * (i + 1));
          var temp = array[i];
          array[i] = array[j];
          array[j] = temp;
        }
        return array;
      }

      (function () {
        var arr = [],
          length = $('.marquee--top .marquee-slider li:not(".spacer")').length;
        for (var i = 0; i < length; ++i) arr[i] = i;

        arr = shuffleArray(arr);

        for (var i = 0; i < arr.length; ++i) {
          var li = $('.marquee--top .marquee-slider li:not(".spacer")').eq(
            arr[i]
          );
          if (li.length > 0)
            $(".marquee--bottom .marquee-slider").append(
              (li.get(0).outerHTML || "") + '<li class="spacer"><i></i></li>'
            );
        }

        marqueeSlider({ ID: ".marquee--top .marquee-slider" });
        marqueeSlider({
          ID: ".marquee--bottom .marquee-slider",
          direction: "right",
        });

